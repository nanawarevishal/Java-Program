
/*
 4. Write a program to print even numbers 1-100
 */

package ForLoop.Assignments.Assignment02;

public class program4 {
    public static void main(String[] args) {

        for(int i=1;i<=100;i++){
            if(i%2==0){
                System.out.print(i+" ");
            }
        }
        
    }
}
