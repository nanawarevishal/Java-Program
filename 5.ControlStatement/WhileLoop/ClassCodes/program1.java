
// Print the integers from 1-10 using while Loop

package WhileLoop.ClassCodes;

public class program1 {
    public static void main(String[] args) {

        // While and For loop are entry controlled loop
        // Do-While is the Exit controlled Loop

        int i = 1;
        while (i <= 10) {
            
            System.out.print((i++ )+ " ");
            
        }
    }
}
