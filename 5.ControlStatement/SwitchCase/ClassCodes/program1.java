package SwitchCase.ClassCodes;


public class program1 {
    public static void main(String[] args) {
         
        int x =2;

        switch (x) {
            case 1:
            System.out.println("One");
            break;
            
            case 2:
            System.out.println("Two");

            case 3:
            System.out.println("three");

            case 4:
            System.out.println("Four");
            
            default:
                System.out.println("a");
                break;
        }
    }
}
