package OneDimenArray.ClassCodeArray;

public class program9 {
    public static void main(String[] args) {
        
        float fa[] = {12f,34f};
        for(int i=0;i<fa.length;i++){
            System.out.println(fa[i]+" ");
        }

        double db[] = {12d,34f};
        for(int i=0;i<db.length;i++){
            System.out.println(db[i]);
        }
    }
}
