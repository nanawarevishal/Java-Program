public class Program11 {
    public static void main(String[] args) {
        
        // Bitwise operator 
        //  &  | <<  >>  >>> ~

        int x =5;   //0000 0101
        int y= 7;   //0000 0111
                    //0000 0111 or
                    //0000 0101 and

        System.out.println(x & y);
        System.out.println(x | y);

    }
}
